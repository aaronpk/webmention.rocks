<div class="ui message">

  <p>You need to set your authorization endpoint in a <code>&lt;link&gt;</code> tag on your home page.</p>

  <p>You can <a href="https://indieweb.org/authorization-endpoint">create your own authorization endpoint</a>, or you can use an existing service such as <a href="https://indieauth.com/">IndieAuth.com</a>. To delegate to IndieAuth.com, place the markup below on your home page (or at the URL you are using as your web sign-in address).</p>

  <pre><code>&lt;link rel="authorization_endpoint"
        href="https://indieauth.com/auth"&gt;</code></pre>

</div>
